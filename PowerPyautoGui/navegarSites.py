# como navegar ate um site

# webbrowser + pyautogui

import webbrowser

# cria uma aba no navegador existente ou criar outro
webbrowser.open('https://google.com')
# Abrir uma nova aba no navegador existente ou criar outro
webbrowser.open_new_tab('https://google.com')
# Criar e abrir uma nova janela no navegador do zero
webbrowser.open_new('https://google.com')
